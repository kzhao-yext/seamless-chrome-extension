'use strict';


function injectTheScript() {
    chrome.tabs.query({active: true, currentWindow: true}, function(tabs) {
        // query the active tab, which will be only one tab
        //and inject the script in it
        chrome.tabs.executeScript(tabs[0].id, {file: "js/click_script.js"});
    });
}


function injectTheScript2() {
    chrome.tabs.query({active: true, currentWindow: true}, function(tabs) {
        // query the active tab, which will be only one tab
        //and inject the script in it
        chrome.tabs.executeScript(tabs[0].id, {file: "js/history_script.js"});
    });
}

document.getElementById('feelingLucky').addEventListener('click', injectTheScript);
document.getElementById('randomOrder').addEventListener('click', injectTheScript2);

